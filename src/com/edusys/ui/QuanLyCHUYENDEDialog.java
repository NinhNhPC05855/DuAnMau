    void chonAnh() {
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) { // hiển thị hộp thoại chọn fill
            File file = fileChooser.getSelectedFile();//lấy đối tượng File , đại diện cho tệp tin được chọn bởi người dùng và gán file
            Ximage.save(file); // lưu tệp vào vị trí cụ thể
            ImageIcon icon = Ximage.read(file.getName()); // đọc tệp tin đã lưu. Kết quả trả về là một đối tượng ImageIcon
            lblHinh.setIcon(icon); // gán đối tượng và hiển thị hình
            lblHinh.setToolTipText(file.getName()); //gợi ý cho đối tượng lblHinh, hiển thị tên tệp tin
        }
    }

