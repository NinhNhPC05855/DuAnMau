//prev

    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }
//next

    void next() {
        if (this.row < tblChuyenDe.getRowCount() - 1) {
            this.row++;
            this.edit();
        }

    }
//last

    void last() {
        this.row = tblChuyenDe.getRowCount() - 1;
        this.edit();
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblChuyenDe.getRowCount() - 1);
        // trạng thái form 
        txtMaCD.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
        // trạng thái điều hướng 
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);

    }

    public boolean checkValidate() {

        if (txtMaCD.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã Chuyên Đề ", "WARNING", JOptionPane.WARNING_MESSAGE);
            txtMaCD.requestFocus();
            return false;
        }

        if (txtTenCD.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Tên Chuyên Đề!", "WARNING", JOptionPane.WARNING_MESSAGE);
            txtTenCD.requestFocus();
            return false;
        }

        if (txtHocPhi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập HỌC Phí", "WARNING", JOptionPane.WARNING_MESSAGE);
            txtHocPhi.requestFocus();
            return false;
        }

        String phonePattern = "^\\d+$";
        String phoneNumber = txtHocPhi.getText();

        if (!phoneNumber.matches(phonePattern)) {
            JOptionPane.showMessageDialog(this, "Học Phí chỉ chấp nhận ký tự số!", "WARNING", JOptionPane.WARNING_MESSAGE);
            txtHocPhi.requestFocus();
            return false;
        }
        if (txtThoiLuong.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập ngày sinh!", "WARNING", JOptionPane.WARNING_MESSAGE);
            txtThoiLuong.requestFocus();
            return false;
        }
        String tl = "^\\d+$";
        String tlString = txtThoiLuong.getText();

        if (!tlString.matches(tl)) {
            JOptionPane.showMessageDialog(this, "Thời Lượng chỉ chấp nhận ký tự số!", "WARNING", JOptionPane.WARNING_MESSAGE);
            txtThoiLuong.requestFocus();
            return false;
        }

        return true;
    }
