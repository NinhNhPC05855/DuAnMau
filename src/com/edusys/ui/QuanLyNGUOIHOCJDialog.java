    void first() {
        this.row = 0;
        this.edit();
    }
//prev

    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }
//next

    void next() {
        if (this.row < tblNguoiHoc.getRowCount() - 1) {
            this.row++;
            this.edit();
        }

    }
//last

    void last() {
        this.row = tblNguoiHoc.getRowCount() - 1;
        this.edit();
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        //Nếu biến row bằng chỉ số cuối cùng, điều này có nghĩa là đang ở hàng cuối cùng (trạng thái "last").
        boolean last = (this.row == tblNguoiHoc.getRowCount() - 1);
        // trạng thái form 
        txtMaNH.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
        // trạng thái điều hướng 
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);

    }

    public boolean checkValidate() {

        if (txtMaNH.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã Người Học ", "WARNING", JOptionPane.WARNING_MESSAGE);
            txtMaNH.requestFocus();
            return false;
        }

        if (txtHoVaTen.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Họ Tên!", "WARNING", JOptionPane.WARNING_MESSAGE);
            txtHoVaTen.requestFocus();
            return false;
        }

        if (!rdoNam.isSelected() && !rdoNu.isSelected()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn Giới Tính!");
            return false;
        }
        if (txtDienThoai.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số điện thoại!", "WARNING", JOptionPane.WARNING_MESSAGE);
            txtDienThoai.requestFocus();
            return false;
        }

        String phonePattern = "^\\d+$";
        String phoneNumber = txtDienThoai.getText();

        if (!phoneNumber.matches(phonePattern)) {
            JOptionPane.showMessageDialog(this, "Số điện thoại chỉ chấp nhận ký tự số!", "WARNING", JOptionPane.WARNING_MESSAGE);
            txtDienThoai.requestFocus();
            return false;
        }
        if (txtNgaySinh.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập ngày sinh!", "WARNING", JOptionPane.WARNING_MESSAGE);
            txtNgaySinh.requestFocus();
            return false;
        }
        if (txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập email!", "WARNING", JOptionPane.WARNING_MESSAGE);
            txtEmail.requestFocus();
            return false;
        }

        String emailPattern = "^\\w+([.-]?\\w+)*@fpt\\.edu\\.vn$";
        String email = txtEmail.getText();

        if (!email.matches(emailPattern)) {
            JOptionPane.showMessageDialog(this, "Email không đúng định dạng! Vui lòng nhập email với domain là @fpt.edu.vn", "WARNING", JOptionPane.WARNING_MESSAGE);
            txtEmail.requestFocus();
            return false;
        }

        return true;
    }
