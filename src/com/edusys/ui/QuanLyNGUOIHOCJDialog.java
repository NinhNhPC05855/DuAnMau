    void init() {
        setLocationRelativeTo(null);
        this.fillTable();
        this.row = -1;
        this.updateStatus();
    }

    void fillTable() {
        // tạo DefaultTable gán cho biến model 
        //getModel() để truy cập đối tượng mô hình của bảng tblNguoiHoc
        DefaultTableModel model = (DefaultTableModel) tblNguoiHoc.getModel();
        //xá các hàng hiện tại có trong bảng tblNguoiHoc
        model.setRowCount(0);
        try {
            //tim kiếm theo HoTen 
            String keywork = txtTimKiem.getText();
            List<NguoiHoc> list = dao.selectByKeyword(keywork);
            for (NguoiHoc nh : list) {
                // tạo mảng row chứa thông tin của đối tượng nh
                Object[] row = {nh.getMaNH(),
                    nh.getHoTen(),
                    nh.isGioiTinh() ? "Nam" : "Nữ",
                    nh.getNgaySinh(),
                    nh.getDienThoai(),
                    nh.getEmail(),
                    nh.getMaNV(),
                    nh.getNgayDK()};

                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu ");
        }
    }

